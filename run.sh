    ==  ==    ==  ===  ========  =  ==  =======  =====  =  ====  ===  ==  ====
    ===    ==    ====  ========  =  ==  =======  =  ==  =  ==  =  ==  ==  =  =
    ====  ====  =====  =========   ===  ========   ===  =  ===   ===  ===   ==
    ==========================================================================
"

$PYBIN wforensic/manage.py runserver

exit $?
